package CoreJava.Assignment_Operators;

public class Ex08 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Increment operator
        a++; // a = a + 1
        System.out.println("Increment operator: " + a);

        // Decrement operator
        b--; // b = b - 1
        System.out.println("Decrement operator: " + b);

        // Addition assignment operator
        a += 5; // a = a + 5
        System.out.println("Addition assignment operator: " + a);

        // Subtraction assignment operator
        b -= 3; // b = b - 3
        System.out.println("Subtraction assignment operator: " + b);

        // Multiplication assignment operator
        a *= 3; // a = a * 3
        System.out.println("Multiplication assignment operator: " + a);

        // Division assignment operator
        b /= 2; // b = b / 2
        System.out.println("Division assignment operator: " + b);
    }
}
