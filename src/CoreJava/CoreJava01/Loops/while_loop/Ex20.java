package CoreJava.CoreJava01.Loops.while_loop;

public class Ex20 {
    public static void main(String[] args) {
        int i=1;
        while (i<=4){
            System.out.println("Hello World " + i);
            System.out.println("Hello");
            i++;  // increment i by 1 after each iteration
        }
        System.out.println("Bye " + i);
    }
}
